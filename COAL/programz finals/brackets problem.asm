.MODEL SMALL
.STACK 100H
.DATA
MSG1 DB 0AH,0DH,'ENTER AN ALGEBRIC EXPRESSION:$'
C1 DB ?
MSG2 DB 0AH , 0DH,'TOO MANY RIGHT BRACKETS.BEGIN AGAIN!$'
MSG3 DB 0AH,0DH,'TOO MANY LEFT BARCKETS.BEGIN AGAIN!$'
MSG4 DB 0AH,0DH,'EXPRESSION IS CORRECT.$'
MSG5 DB 0AH,0DH,'TYPE Y IF YOU WANT TO CONTINUE:$'
C2 DB ?
.CODE
MAIN PROC

MOV AX,@DATA
MOV DS, AX

MOV AH , 9
LEA DX, MSG1
INT 21H

REPEAT:
MOV AH, 1
MOV C1,AH
INT 21H

CMP AL,'('
JE IN_1

CMP AL,'{'
JE IN_1

CMP AL,'['
JE IN_1

CMP AL,')'
JE OUT_1

CMP AL,'}'
JE OUT_1

CMP AL,']'
JE OUT_1

JMP REPEAT

IN_1:
PUSH AX

MOV AH,9
MOV DX,MSG4
INT 21H

OUT_1:
POP BX
SUB BL,1
CMP BL,AL

MOV AH,9
MOV DX,MSG5
INT 21H

CMP AX,'y'
JE REPEAT

MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN


