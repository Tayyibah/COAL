.MODEL SMALL
.STACK 100H
.DATA
	A DW 0
	B DW 1
	COMMA DB ','
.CODE
MAIN PROC
	MOV AX,@DATA ; initialize DS
	MOV DS,AX

	MOV AX,A

 	MOV AX,2
	MOV DX,AX
	INT 21H

	MOV AX,9
	LEA DX,COMMA
	INT 21H

	ADD B ,AX

	MOV DX,B
	INT 21H
	
	LEA DX,COMMA
	INT 21H
	

MOV AH,4CH ; return to DOS
INT 21H
MAIN ENDP
END MAIN